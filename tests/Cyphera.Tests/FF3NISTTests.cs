using Xunit;
using Cyphera;

namespace Cyphera.Tests
{
    public class FF3NISTTests
    {
        static byte[] Hex(string h) => FF3.HexToBytes(h);
        const string R26 = "0123456789abcdefghijklmnop";

        void Nist(string keyHex, string tweakHex, string alphabet, string pt, string ct)
        {
            var c = new FF3(Hex(keyHex), Hex(tweakHex), alphabet);
            Assert.Equal(ct, c.Encrypt(pt));
            Assert.Equal(pt, c.Decrypt(ct));
        }

        [Fact] public void S01() => Nist("EF4359D8D580AA4F7F036D6F04FC6A94", "D8E7920AFA330A73", "0123456789", "890121234567890000", "750918814058654607");
        [Fact] public void S02() => Nist("EF4359D8D580AA4F7F036D6F04FC6A94", "9A768A92F60E12D8", "0123456789", "890121234567890000", "018989839189395384");
        [Fact] public void S03() => Nist("EF4359D8D580AA4F7F036D6F04FC6A94", "D8E7920AFA330A73", "0123456789", "89012123456789000000789000000", "48598367162252569629397416226");
